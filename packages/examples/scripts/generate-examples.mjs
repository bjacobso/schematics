import { execFile } from "node:child_process";
import { mkdir, readdir, readFile, writeFile } from "node:fs/promises";
import { dirname, join, relative } from "node:path";
import { promisify } from "node:util";
import { fileURLToPath } from "node:url";

const packageDir = dirname(dirname(fileURLToPath(import.meta.url)));
const repoRoot = join(packageDir, "../..");
const workspacesDir = join(packageDir, "workspaces");
const outputPath = join(packageDir, "src/generated/examples.ts");
const execFileAsync = promisify(execFile);
const workspaceRoots = [workspacesDir, join(repoRoot, "packages/onboarded-config/workspaces")];

const definitions = await readExampleDefinitions();
const examples = await Promise.all(
  definitions.map(async (entry) => {
    const filesDirectory = join(entry.absoluteDirectory, "files");
    const files = await readExampleFiles(filesDirectory, entry.fileOrder ?? []);

    return {
      id: entry.id,
      name: entry.name,
      description: entry.description,
      project: `__PROJECT__${entry.project}`,
      schema: `__SCHEMA__${entry.schema}`,
      defaultFormat: entry.defaultFormat,
      suggestedPrompts: entry.suggestedPrompts,
      files,
    };
  }),
);
const bundledDefinitions = definitions.map((entry) => ({
  id: entry.id,
  name: entry.name,
  description: entry.description,
  project: `__PROJECT__${entry.project}`,
  schema: `__SCHEMA__${entry.schema}`,
  defaultFormat: entry.defaultFormat,
  suggestedPrompts: entry.suggestedPrompts,
  directory: entry.directory,
  filesPath: entry.filesPath,
  configPath: entry.configPath,
}));
const schemaImports = [...new Set(definitions.map((entry) => entry.schema))].sort();
const projectImports = [...new Set(definitions.map((entry) => entry.project))].sort();
const symbolImports = [...new Set([...projectImports, ...schemaImports])].sort();
const serializedExamples = replaceSymbols(JSON.stringify(examples, null, 2));
const serializedDefinitions = replaceSymbols(JSON.stringify(bundledDefinitions, null, 2));

const source = `/* This file is generated by packages/examples/scripts/generate-examples.mjs. */
import type { ArtifactProjectDeclaration } from "@schema-ide/artifacts";
import type { SourceFile, WorkspaceSchema } from "@schema-ide/core";
import { ${symbolImports.join(", ")} } from "../schemas";

export interface SchemaIdeExample {
  readonly id: string;
  readonly name: string;
  readonly description: string;
  readonly project: ArtifactProjectDeclaration<string, any, any>;
  readonly schema: WorkspaceSchema<unknown>;
  readonly files: readonly SourceFile[];
  readonly defaultFormat?: "json" | "yaml" | undefined;
  readonly suggestedPrompts?: readonly string[] | undefined;
}

export interface SchemaIdeExampleWorkspaceDefinition {
  readonly id: string;
  readonly name: string;
  readonly description: string;
  readonly project: ArtifactProjectDeclaration<string, any, any>;
  readonly schema: WorkspaceSchema<unknown>;
  readonly defaultFormat?: "json" | "yaml" | undefined;
  readonly suggestedPrompts?: readonly string[] | undefined;
  readonly directory: string;
  readonly filesPath: string;
  readonly configPath: string;
}

export const schemaIdeExampleDefinitions = ${serializedDefinitions} satisfies readonly SchemaIdeExampleWorkspaceDefinition[];

export const schemaIdeExamples = ${serializedExamples} satisfies readonly SchemaIdeExample[];
`;

await mkdir(dirname(outputPath), { recursive: true });
await writeFile(outputPath, source);
await execFileAsync("pnpm", ["exec", "oxfmt", outputPath], { cwd: packageDir });

function replaceSymbols(text) {
  return text.replace(/"__(?:PROJECT|SCHEMA)__(\w+)"/g, "$1");
}

async function readExampleDefinitions() {
  const definitions = (
    await Promise.all(
      workspaceRoots.map((workspaceRoot) => readExampleDefinitionsFrom(workspaceRoot)),
    )
  ).flat();

  return definitions.sort((left, right) => left.id.localeCompare(right.id));
}

async function readExampleDefinitionsFrom(workspaceRoot) {
  const entries = await readdir(workspaceRoot, { withFileTypes: true });
  return Promise.all(
    entries
      .filter((entry) => entry.isDirectory())
      .map(async (entry) => {
        const absoluteDirectory = join(workspaceRoot, entry.name);
        const relativeDirectory = relative(packageDir, absoluteDirectory).split("\\").join("/");
        return {
          directory: entry.name,
          absoluteDirectory,
          filesPath: `${relativeDirectory}/files`,
          configPath: `${relativeDirectory}/schema-ide.config.ts`,
          ...JSON.parse(await readFile(join(absoluteDirectory, "example.json"), "utf8")),
        };
      }),
  );
}

async function readExampleFiles(directory, fileOrder) {
  const paths = await collectFiles(directory, directory);
  return Promise.all(
    paths
      .sort((left, right) => compareExamplePaths(left, right, fileOrder))
      .map(async (path) => ({
        path,
        content: await readExampleFile(join(directory, path), path),
      })),
  );
}

async function readExampleFile(absolutePath, path) {
  if (isBinaryExamplePath(path)) {
    return (await readFile(absolutePath)).toString("base64");
  }

  return readFile(absolutePath, "utf8");
}

function isBinaryExamplePath(path) {
  return /\.(?:pdf|png|jpe?g|webp)$/i.test(path);
}

function compareExamplePaths(left, right, fileOrder) {
  const leftIndex = orderIndex(left, fileOrder);
  const rightIndex = orderIndex(right, fileOrder);

  if (leftIndex !== rightIndex) {
    return leftIndex - rightIndex;
  }

  return left.localeCompare(right);
}

function orderIndex(path, fileOrder) {
  const index = fileOrder.findIndex((prefix) => path.startsWith(prefix));
  return index === -1 ? fileOrder.length : index;
}

async function collectFiles(root, directory) {
  const entries = await readdir(directory, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const absolutePath = join(directory, entry.name);
    if (entry.isDirectory()) {
      files.push(...(await collectFiles(root, absolutePath)));
      continue;
    }
    if (entry.isFile()) {
      files.push(relative(root, absolutePath).split("\\").join("/"));
    }
  }

  return files;
}
